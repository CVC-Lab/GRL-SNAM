# Hamiltonian / pH surrogate
# -----------------------------------------------------------------------------

def icp_barrier(d: torch.Tensor, d_hat: float = 0.90, eps: float = 1e-6, max_b: float = 50.0) -> torch.Tensor:
    """Smooth clipped IPC-like barrier basis B(d), active for 0 < d < d_hat.

    d is clearance after subtracting pseudo-obstacle radius and robot radius.
    The basis is fixed; the network only predicts its coefficients.
    """
    dh = d.new_tensor(float(d_hat))
    safe = torch.clamp(d, min=eps)
    b_in = -(d - dh) ** 2 * torch.log(safe / dh)
    b = torch.where(d < dh, b_in, torch.zeros_like(d))
    # A finite inside-obstacle continuation: strong but not numerically fatal.
    b = torch.where(d <= eps, dh * dh * (-torch.log(dh.new_tensor(eps) / dh)) + (eps - d) ** 2, b)
    return torch.clamp(b, 0.0, max_b)


def local_teacher_pose_sequence(pose0: torch.Tensor, pose_seq: torch.Tensor) -> torch.Tensor:
    """Map world teacher poses [B,H,3] into the initial robot frame."""
    x0, y0, th0 = pose0[:, 0], pose0[:, 1], pose0[:, 2]
    dx = pose_seq[..., 0] - x0[:, None]
    dy = pose_seq[..., 1] - y0[:, None]
    c = torch.cos(th0)[:, None]
    s = torch.sin(th0)[:, None]
    # R^T [dx,dy]
    lx = c * dx + s * dy
    ly = -s * dx + c * dy
    lth = torch.atan2(torch.sin(pose_seq[..., 2] - th0[:, None]), torch.cos(pose_seq[..., 2] - th0[:, None]))
    return torch.stack([lx, ly, lth], dim=-1)


def effective_stage_target(goal: torch.Tensor, obs_feats: torch.Tensor, obs_mask: torch.Tensor, coef: Dict[str, torch.Tensor]) -> torch.Tensor:
    """Blend the goal with a tangential escape target for stage/overlap transit.

    This is a local surrogate for in-between-stage reshaping. If an obstacle is
    frontal/near, the target is nudged along the boundary tangent that points
    most consistently toward the goal. stage_mix controls the blend.
    """
    B, N = obs_feats.shape[:2]
    if N == 0:
        return goal
    c = obs_feats[..., :2]
    rnorm = torch.linalg.norm(c, dim=-1).clamp_min(1e-6)
    front_gate = ((c[..., 0] > -0.20) & (torch.abs(c[..., 1]) < 1.25) & obs_mask).float()
    prox = obs_feats[..., 5] * front_gate
    nearest = prox.argmax(dim=1)
    idx = nearest[:, None, None].expand(-1, 1, 2)
    c_sel = torch.gather(c, 1, idx).squeeze(1)
    n = c_sel / torch.linalg.norm(c_sel, dim=-1, keepdim=True).clamp_min(1e-6)
    t = torch.stack([-n[:, 1], n[:, 0]], dim=-1)
    sign = torch.sign((t * goal).sum(dim=-1, keepdim=True) + 1e-6)
    escape = goal + sign * t * (0.75 + 0.50 * coef["stage_mix"].unsqueeze(-1))
    rho = coef["stage_mix"].unsqueeze(-1)
    return (1.0 - rho) * goal + rho * escape


def shaped_potential(q: torch.Tensor, p: torch.Tensor, obs_feats: torch.Tensor, obs_mask: torch.Tensor,
                     goal_feats: torch.Tensor, coef: Dict[str, torch.Tensor], robot_radius: float) -> torch.Tensor:
    goal = goal_feats[:, :2]
    stage_goal = effective_stage_target(goal, obs_feats, obs_mask, coef)
    V_goal = 0.5 * coef["beta_goal"] * ((q - goal) ** 2).sum(dim=-1)
    V_stage = 0.5 * coef["stage_gain"] * ((q - stage_goal) ** 2).sum(dim=-1)

    if obs_feats.shape[1] > 0:
        C = obs_feats[..., :2]
        R = obs_feats[..., 2]
        d = torch.linalg.norm(q[:, None, :] - C, dim=-1) - R - float(robot_radius)
        B_icp = icp_barrier(d)
        V_icp = (coef["beta_icp"].unsqueeze(-1) * coef["alphas"] * B_icp * obs_mask.float()).sum(dim=-1)
    else:
        V_icp = torch.zeros_like(V_goal)

    # Trap-memory bump: activated by stall feature; its gradient is intentionally
    # slightly shifted behind the vehicle so it pushes out of quasi-static traps.
    stall = goal_feats[:, 7].clamp(0.0, 1.0)
    mem_center = torch.stack([-0.20 * torch.ones_like(stall), torch.zeros_like(stall)], dim=-1)
    sigma2 = 0.35 ** 2
    V_mem = stall * coef["memory_amp"] * torch.exp(-((q - mem_center) ** 2).sum(dim=-1) / (2.0 * sigma2))
    return V_goal + V_stage + V_icp + V_mem


def tangential_port(q: torch.Tensor, obs_feats: torch.Tensor, obs_mask: torch.Tensor,
                    goal_feats: torch.Tensor, coef: Dict[str, torch.Tensor]) -> torch.Tensor:
    if obs_feats.shape[1] == 0:
        return torch.zeros_like(q)
    C = obs_feats[..., :2]
    R = obs_feats[..., 2]
    rel = q[:, None, :] - C
    norm = torch.linalg.norm(rel, dim=-1).clamp_min(1e-6)
    n = rel / norm.unsqueeze(-1)
    t = torch.stack([-n[..., 1], n[..., 0]], dim=-1)
    goal_dir = goal_feats[:, None, :2] - q[:, None, :]
    sign = torch.sign((t * goal_dir).sum(dim=-1, keepdim=True) + 1e-6)
    clear = norm - R
    gate = torch.relu(1.25 - clear) / 1.25
    front = ((C[..., 0] > -0.25) & (torch.abs(C[..., 1]) < 1.50) & obs_mask).float()
    w = gate * front * coef["alphas"]
    port = (w.unsqueeze(-1) * sign * t).sum(dim=1)
    return coef["tangent_gain"].unsqueeze(-1) * port


def integrate_ph(obs_feats: torch.Tensor, obs_mask: torch.Tensor, goal_feats: torch.Tensor,
                 coef: Dict[str, torch.Tensor], horizon: int, dt: float, robot_radius: float,
                 return_energy: bool = False) -> Dict[str, torch.Tensor]:
    B = goal_feats.shape[0]
    q = torch.zeros(B, 2, device=goal_feats.device, dtype=goal_feats.dtype)
    # initial virtual momentum uses measured speed along local x-axis.
    p = torch.stack([goal_feats[:, 4], torch.zeros_like(goal_feats[:, 4])], dim=-1)
    qs, ps, Hs = [], [], []
    for _ in range(int(horizon)):
        q = q.requires_grad_(True)
        V = shaped_potential(q, p, obs_feats, obs_mask, goal_feats, coef, robot_radius)
        grad_q = torch.autograd.grad(V.sum(), q, create_graph=True, retain_graph=True)[0]
        port = tangential_port(q, obs_feats, obs_mask, goal_feats, coef)
        p = p + dt * (-grad_q - coef["gamma"].unsqueeze(-1) * p + port)
        # clamp virtual velocity to avoid exploding surrogate rollouts.
        p_norm = torch.linalg.norm(p, dim=-1, keepdim=True).clamp_min(1e-6)
        p = p * torch.clamp(1.2 / p_norm, max=1.0)
        q = q + dt * p
        qs.append(q)
        ps.append(p)
        if return_energy:
            K = 0.5 * (p * p).sum(dim=-1)
            Hs.append(K + shaped_potential(q, p, obs_feats, obs_mask, goal_feats, coef, robot_radius))
    out = {"q_seq": torch.stack(qs, dim=1), "p_seq": torch.stack(ps, dim=1)}
    if return_energy:
        out["H_seq"] = torch.stack(Hs, dim=1)
    return out


def command_from_ph(obs_feats: torch.Tensor, obs_mask: torch.Tensor, goal_feats: torch.Tensor,
                    coef: Dict[str, torch.Tensor], ack_cfg: AckermannCfg, robot_radius: float) -> torch.Tensor:
    """Convert the current pH vector field at q=0 into an Ackermann twist."""
    B = goal_feats.shape[0]
    q = torch.zeros(B, 2, device=goal_feats.device, dtype=goal_feats.dtype, requires_grad=True)
    p = torch.stack([goal_feats[:, 4], torch.zeros_like(goal_feats[:, 4])], dim=-1)
    V = shaped_potential(q, p, obs_feats, obs_mask, goal_feats, coef, robot_radius)
    grad_q = torch.autograd.grad(V.sum(), q, create_graph=True, retain_graph=True)[0]
    port = tangential_port(q, obs_feats, obs_mask, goal_feats, coef)
    a = -grad_q - coef["gamma"].unsqueeze(-1) * p + port
    desired = p + ack_cfg.dt * a
    desired_angle = torch.atan2(desired[:, 1], desired[:, 0])
    speed = torch.linalg.norm(desired, dim=-1)
    # signed reverse when stall is high and frontal clearance is low: this is a
    # pH memory/port-induced sign, not a direct action head.
    stall = goal_feats[:, 7]
    front_clear = goal_feats[:, 6]
    reverse_gate = ((stall > 0.55) & (front_clear < 0.65)).to(speed.dtype)
    speed_signed = (1.0 - reverse_gate) * speed - reverse_gate * torch.clamp(speed, 0.08, ack_cfg.v_reverse_max)
    omega = 1.8 * torch.atan2(torch.sin(desired_angle), torch.cos(desired_angle))
    raw = torch.stack([speed_signed, omega], dim=-1)
    v, om, _ = project_twist_torch(raw, ack_cfg)
    return torch.stack([v, om], dim=-1)


