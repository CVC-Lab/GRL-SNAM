"""
Train a Stage-1 pH/Hamiltonian coefficient model for Ackermann navigation.

This restores the intended learning object:

    scan/local context -> Hamiltonian/ICP coefficients -> pH surrogate flow
                         -> Ackermann command adapter

The neural network does not directly learn an action sequence. It predicts
context-dependent coefficients for a shaped potential

    V(q) = beta_g V_goal(q) + sum_j alpha_j B_ICP(d_j(q))
           + stage_gain V_stage(q) + memory_amp V_mem(q),

plus damping and tangential-port gains. The training loss compares a short pH
surrogate rollout to the teacher rollout generated by the Stage-1 simulator.
"""
