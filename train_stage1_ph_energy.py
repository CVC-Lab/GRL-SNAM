class PositiveHead(nn.Module):
    def __init__(self, d_in: int, lo: float = 1e-4, scale: float = 1.0):
        super().__init__()
        self.net = nn.Sequential(nn.Linear(d_in, 96), nn.ReLU(), nn.Linear(96, 1))
        self.lo = float(lo)
        self.scale = float(scale)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.lo + self.scale * F.softplus(self.net(x)).squeeze(-1)


class StagePHCoefNet(nn.Module):
    """Context-conditioned Hamiltonian/ICP coefficient network.

    Inputs:
      obs_feats:  [B,N,6] pseudo-obstacle tokens in robot/body frame
      obs_mask:   [B,N]
      goal_feats: [B,8] local goal/history features

    Outputs:
      alphas:       [B,N] obstacle ICP weights
      beta_goal:    [B]   goal-potential strength
      beta_icp:     [B]   global soft ICP temperature/gain
      gamma:        [B]   viscous damping in virtual pH flow
      tangent_gain: [B]   nonconservative tangential port gain
      stage_mix:    [B]   soft blend toward tangent/transition target
      stage_gain:   [B]   transition potential gain
      memory_amp:   [B]   trap-memory bump amplitude
    """
    def __init__(self, d_obs: int = 6, d_goal: int = 8, d_tok: int = 96, n_layers: int = 2):
        super().__init__()
        self.d_tok = int(d_tok)
        self.obs_enc = nn.Sequential(
            nn.Linear(d_obs, 128), nn.ReLU(), nn.Linear(128, d_tok), nn.ReLU()
        )
        self.goal_enc = nn.Sequential(
            nn.Linear(d_goal, 128), nn.ReLU(), nn.Linear(128, d_tok), nn.ReLU()
        )
        layer = nn.TransformerEncoderLayer(
            d_model=d_tok, nhead=4, dim_feedforward=192, batch_first=True
        )
        self.fuser = nn.TransformerEncoder(layer, num_layers=n_layers)
        self.alpha_head = nn.Sequential(nn.Linear(d_tok, 64), nn.ReLU(), nn.Linear(64, 1))
        self.beta_goal_head = PositiveHead(d_tok, scale=1.25)
        self.beta_icp_head = PositiveHead(d_tok, scale=1.25)
        self.gamma_head = PositiveHead(d_tok, scale=0.75)
        self.tangent_head = PositiveHead(d_tok, scale=1.00)
        self.stage_gain_head = PositiveHead(d_tok, scale=0.75)
        self.memory_amp_head = PositiveHead(d_tok, scale=0.50)
        self.stage_mix_head = nn.Sequential(nn.Linear(d_tok, 96), nn.ReLU(), nn.Linear(96, 1))
        self.value_head = nn.Sequential(nn.Linear(d_tok, 96), nn.ReLU(), nn.Linear(96, 1))

    def encode(self, obs_feats: torch.Tensor, obs_mask: torch.Tensor, goal_feats: torch.Tensor) -> Tuple[torch.Tensor, torch.Tensor]:
        B, N = obs_feats.shape[:2]
        z_goal = self.goal_enc(goal_feats).unsqueeze(1)
        if N == 0:
            z_obs = obs_feats.new_zeros(B, 0, self.d_tok)
            tokens = z_goal
            pad = torch.zeros(B, 1, dtype=torch.bool, device=goal_feats.device)
        else:
            z_obs = self.obs_enc(obs_feats.reshape(B * N, -1)).reshape(B, N, -1)
            tokens = torch.cat([z_goal, z_obs], dim=1)
            pad = torch.cat([torch.zeros(B, 1, dtype=torch.bool, device=obs_mask.device), ~obs_mask], dim=1)
        z = self.fuser(tokens, src_key_padding_mask=pad)
        return z[:, 0], z_obs

    def forward(self, obs_feats: torch.Tensor, obs_mask: torch.Tensor, goal_feats: torch.Tensor) -> Dict[str, torch.Tensor]:
        ctx, z_obs = self.encode(obs_feats, obs_mask, goal_feats)
        if z_obs.shape[1] == 0:
            alphas = obs_mask.new_zeros(obs_mask.shape, dtype=goal_feats.dtype)
        else:
            alphas = F.softplus(self.alpha_head(z_obs).squeeze(-1))
            alphas = torch.where(obs_mask, alphas, torch.zeros_like(alphas))
        return {
            "alphas": alphas,
            "beta_goal": self.beta_goal_head(ctx),
            "beta_icp": self.beta_icp_head(ctx),
            "gamma": self.gamma_head(ctx),
            "tangent_gain": self.tangent_head(ctx),
            "stage_mix": torch.sigmoid(self.stage_mix_head(ctx)).squeeze(-1),
            "stage_gain": self.stage_gain_head(ctx),
            "memory_amp": self.memory_amp_head(ctx),
            "value": self.value_head(ctx).squeeze(-1),
        }


