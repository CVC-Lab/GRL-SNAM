def clearance_penalty(q_seq: torch.Tensor, obs_feats: torch.Tensor, obs_mask: torch.Tensor, robot_radius: float) -> torch.Tensor:
    if obs_feats.shape[1] == 0:
        return q_seq.new_zeros(())
    C = obs_feats[:, None, :, :2]
    R = obs_feats[:, None, :, 2]
    q = q_seq[:, :, None, :]
    clear = torch.linalg.norm(q - C, dim=-1) - R - float(robot_radius)
    clear = torch.where(obs_mask[:, None, :], clear, torch.full_like(clear, 1e3))
    min_clear = clear.min(dim=-1).values
    return (torch.relu(0.05 - min_clear) ** 2).mean()


def run_epoch(model: StagePHCoefNet, loader: DataLoader, train_cfg: TrainPHCfg, sim_cfg: Stage1Cfg,
              optimizer: torch.optim.Optimizer | None = None) -> Dict[str, float]:
    is_train = optimizer is not None
    model.train(is_train)
    logs = {"loss": 0.0, "roll": 0.0, "final": 0.0, "cmd": 0.0, "pass": 0.0, "clear": 0.0,
            "beta": 0.0, "coef": 0.0, "cmd_mae_v": 0.0, "cmd_mae_om": 0.0,
            "mean_alpha": 0.0, "mean_gamma": 0.0, "mean_stage_mix": 0.0}
    n = 0
    for batch in loader:
        obs = batch["obs_feats"].to(train_cfg.device)
        mask = batch["obs_mask"].to(train_cfg.device)
        goal = batch["goal_feats"].to(train_cfg.device)
        pose = batch["pose"].to(train_cfg.device)
        pose_seq = batch["pose_seq"].to(train_cfg.device)
        cmd_tgt = batch["cmd"].to(train_cfg.device)
        beta_tgt = batch["beta_seq"].to(train_cfg.device)

        coef = model(obs, mask, goal)
        out = integrate_ph(obs, mask, goal, coef, train_cfg.horizon, sim_cfg.ackermann.dt,
                           sim_cfg.scene.robot_radius, return_energy=True)
        target_local = local_teacher_pose_sequence(pose, pose_seq)
        q_seq = out["q_seq"]
        H_seq = out["H_seq"]
        raw_cmd = command_from_ph(obs, mask, goal, coef, sim_cfg.ackermann, sim_cfg.scene.robot_radius)

        L_roll = F.mse_loss(q_seq, target_local[..., :2])
        L_final = F.mse_loss(q_seq[:, -1, :], target_local[:, -1, :2])
        L_cmd = F.mse_loss(raw_cmd, cmd_tgt)
        dH = H_seq[:, 1:] - H_seq[:, :-1]
        L_pass = torch.relu(dH + 0.01 * (out["p_seq"][:, :-1, :] ** 2).sum(dim=-1)).mean() if H_seq.shape[1] > 1 else H_seq.new_zeros(())
        L_clear = clearance_penalty(q_seq, obs, mask, sim_cfg.scene.robot_radius)
        # beta teacher supervises soft ICP gain level rather than direct actions.
        beta_mean_tgt = beta_tgt.mean(dim=1)
        beta_pred = torch.clamp(coef["beta_icp"], 0.0, 4.0) / 4.0
        L_beta = F.mse_loss(beta_pred, beta_mean_tgt)
        L_coef = (coef["alphas"].mean() + coef["beta_goal"].mean() + coef["beta_icp"].mean() + coef["tangent_gain"].mean())
        L = (train_cfg.w_roll * L_roll + train_cfg.w_final * L_final + train_cfg.w_cmd * L_cmd
             + train_cfg.w_pass * L_pass + train_cfg.w_clear * L_clear + train_cfg.w_beta * L_beta
             + train_cfg.w_coef * L_coef)

        if is_train:
            optimizer.zero_grad(set_to_none=True)
            L.backward()
            nn.utils.clip_grad_norm_(model.parameters(), 5.0)
            optimizer.step()

        with torch.no_grad():
            err = torch.abs(raw_cmd - cmd_tgt)
            logs["loss"] += float(L.item())
            logs["roll"] += float(L_roll.item())
            logs["final"] += float(L_final.item())
            logs["cmd"] += float(L_cmd.item())
            logs["pass"] += float(L_pass.item())
            logs["clear"] += float(L_clear.item())
            logs["beta"] += float(L_beta.item())
            logs["coef"] += float(L_coef.item())
            logs["cmd_mae_v"] += float(err[:, 0].mean().item())
            logs["cmd_mae_om"] += float(err[:, 1].mean().item())
            logs["mean_alpha"] += float(coef["alphas"].mean().item()) if coef["alphas"].numel() else 0.0
            logs["mean_gamma"] += float(coef["gamma"].mean().item())
            logs["mean_stage_mix"] += float(coef["stage_mix"].mean().item())
        n += 1
    for k in logs:
        logs[k] /= max(1, n)
    return logs


